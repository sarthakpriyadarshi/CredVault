"use client"

import { useState } from "react"
import { Plus, Minus } from "lucide-react"
import { motion, AnimatePresence } from "framer-motion"

export function FAQSection() {
  const [openItems, setOpenItems] = useState<number[]>([])

  const toggleItem = (index: number) => {
    setOpenItems((prev) => (prev.includes(index) ? prev.filter((i) => i !== index) : [...prev, index]))
  }

  const faqs = [
    {
      question: "What is CredVault?",
      answer:
        "CredVault is a credential and badge issuance platform that makes it easy for organizations to issue digital certificates and badges to recipients. Similar to Credly and Accredible, CredVault allows issuers to create custom templates, issue credentials in bulk, and provides recipients with a dashboard to view and verify their credentials.",
    },
    {
      question: "How does the organization verification process work?",
      answer:
        "Organizations first register on the platform. Their registration request goes to the admin panel for manual verification. Once approved, the organization can sign in, create certificate and badge templates, and start issuing credentials to recipients.",
    },
    {
      question: "How do I create a credential template?",
      answer:
        "After signing in as a verified issuer, click 'Create Template' and upload your certificate or badge image (PDF or PNG). Then add placeholders by defining field names, types (number, ID, text, etc.), and their X/Y coordinates on the canvas. You can organize templates by categories like Security, General, or create custom categories.",
    },
    {
      question: "Can I issue credentials in bulk?",
      answer:
        "Yes! You can upload a CSV file where each column maps to a template field. Email ID is a mandatory column. The system will automatically generate and send certificates or badges to all recipients listed in the CSV file.",
    },
    {
      question: "What's the difference between blockchain and non-blockchain credentials?",
      answer:
        "Non-blockchain credentials are verified through our database. Blockchain credentials are stored on a blockchain network, providing immutable proof of authenticity. You can choose either option when issuing credentials, and can later migrate credentials to blockchain if needed.",
    },
    {
      question: "Can I issue both certificates and badges?",
      answer:
        "Absolutely! When creating a template, you can choose to issue just a certificate, just a badge, or both. If selecting both, you'll need to upload both the certificate and badge images (PDF or PNG) during template creation.",
    },
    {
      question: "How do recipients verify their credentials?",
      answer:
        "Recipients can log in to their dashboard and view all issued credentials in different tabs (All Credentials, With Blockchain, About to Expire, etc.). Clicking on any credential redirects to a verification page that shows credential details and validates authenticity - either through our database or blockchain network, depending on how it was issued.",
    },
  ]

  return (
    <section id="faq" className="relative overflow-hidden pb-120 pt-24">
      {/* Background blur effects */}
      <div className="bg-primary/20 absolute top-1/2 -right-20 z-[-1] h-64 w-64 rounded-full opacity-80 blur-3xl"></div>
      <div className="bg-primary/20 absolute top-1/2 -left-20 z-[-1] h-64 w-64 rounded-full opacity-80 blur-3xl"></div>

      <div className="z-10 container mx-auto px-4">
        <motion.div
          className="flex justify-center"
          initial={{ opacity: 0, y: 20 }}
          whileInView={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.6 }}
          viewport={{ once: true }}
        >
          <div className="border-primary/40 text-primary inline-flex items-center gap-2 rounded-full border px-3 py-1 uppercase">
            <span>âœ¶</span>
            <span className="text-sm">Faqs</span>
          </div>
        </motion.div>

        <motion.h2
          className="mx-auto mt-6 max-w-xl text-center text-4xl font-medium md:text-[54px] md:leading-[60px]"
          initial={{ opacity: 0, y: 20 }}
          whileInView={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.6, delay: 0.2 }}
          viewport={{ once: true }}
        >
          Questions? We&apos;ve got{" "}
          <span className="bg-gradient-to-b from-foreground via-rose-200 to-primary bg-clip-text text-transparent">
            answers
          </span>
        </motion.h2>

        <div className="mx-auto mt-12 flex max-w-xl flex-col gap-6">
          {faqs.map((faq, index) => (
            <motion.div
              key={index}
              className="from-secondary/40 to-secondary/10 rounded-2xl border border-white/10 bg-gradient-to-b p-6 shadow-[0px_2px_0px_0px_rgba(255,255,255,0.1)_inset] transition-all duration-300 hover:border-white/20 cursor-pointer"
              initial={{ opacity: 0, y: 20 }}
              whileInView={{ opacity: 1, y: 0 }}
              transition={{ duration: 0.5, delay: index * 0.1 }}
              viewport={{ once: true }}
              whileHover={{ scale: 1.02 }}
              whileTap={{ scale: 0.98 }}
              onClick={() => toggleItem(index)}
              role="button"
              tabIndex={0}
              onKeyDown={(e) => {
                if (e.key === "Enter" || e.key === " ") {
                  e.preventDefault()
                  toggleItem(index)
                }
              }}
              {...(index === faqs.length - 1 && { "data-faq": faq.question })}
            >
              <div className="flex items-start justify-between">
                <h3 className="m-0 font-medium pr-4">{faq.question}</h3>
                <motion.div
                  animate={{ rotate: openItems.includes(index) ? 180 : 0 }}
                  transition={{ duration: 0.3, ease: "easeInOut" }}
                  className=""
                >
                  {openItems.includes(index) ? (
                    <Minus className="text-primary flex-shrink-0 transition duration-300" size={24} />
                  ) : (
                    <Plus className="text-primary flex-shrink-0 transition duration-300" size={24} />
                  )}
                </motion.div>
              </div>
              <AnimatePresence>
                {openItems.includes(index) && (
                  <motion.div
                    className="mt-4 text-muted-foreground leading-relaxed overflow-hidden"
                    initial={{ opacity: 0, height: 0, marginTop: 0 }}
                    animate={{ opacity: 1, height: "auto", marginTop: 16 }}
                    exit={{ opacity: 0, height: 0, marginTop: 0 }}
                    transition={{
                      duration: 0.4,
                      ease: "easeInOut",
                      opacity: { duration: 0.2 },
                    }}
                  >
                    {faq.answer}
                  </motion.div>
                )}
              </AnimatePresence>
            </motion.div>
          ))}
        </div>
      </div>
    </section>
  )
}
